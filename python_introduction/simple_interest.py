# This is a simple interest calculator
# This value represent $1000 of the principal value
principal = 1000

# This value represents 5% of the value of annual interest rate
rate = 0.05

# This value represents the time duration of 3 years
time = 3

interest = principal * rate * time # This value represents the total value of interest
    
print (f'The simple interest is', {interest}) # This displays the output of the interest in a sentence
